#include <iostream>

int floor[1000][1000];

using namespace std;
int main(int argc, const char * argv[]) {
    int height,width;
    
    for (int i = 0; i < height; i++){
        for (int j = 0; j < width; j++){
            cin >> floor[i][j] ;
        }
    }

    return 0;
}
